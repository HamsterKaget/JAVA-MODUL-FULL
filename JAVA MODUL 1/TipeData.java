public class TipeData {
    
    public static void main(String[] args) {
        

        // Tipe Data Numerik 

        /* Byte ( Menyimpan bilangan bulat ) {
            > Ukuran Memory 8 bit
            > Nilai Minimal = -128
            > Nilai Maksimal = 127
        } */
        byte nilaiByte = 64;
        System.out.println("Nilai Byte = " + nilaiByte);

        /* Short ( Menyimpan bilangan bulat ) {
            > Ukuran Memory 16 bit
            > Nilai Minimal = -32768
            > Nilai Maksimal = 32767
        }*/
        short nilaiShort = 2022;
        System.out.println("Nilai Short = " + nilaiShort);

        /* Integer ( Menyimpan bilangan bulat ) {
            > Ukuran memori 32 bit
            > Nilai Minimal = -2147483648
            > Nilai Maksimal = 2147483647
        } */
        int nilaiInteger = 15000;
        System.out.println("Nilai Integer = " + nilaiInteger);

        /* Long ( Menyimpan bilangan bulat ) {
            > Ukuran memori 64 bit
            > Nilai Minimal = -9223372036854775808
            > Nilai Maksimal = 9223372036854775807
        } */
        long nilaiLong = 2000000;
        System.out.println("Nilai Long = " + nilaiLong);

        /* Float ( Menyimpan bilangan desimal ) {
            > Ukuran memori 32 bit
            > Default value = 0.0f
        } */
        float nilaiFloat = 90.5f;
        System.out.println("Nilai Float = " + nilaiFloat);

        /* Double ( Menyimpan bilangan desimal ) {
            > Ukuran memori 64 bit
            > Default value = 0.0
        } */
        double nilaiDouble = 1090.54;
        System.out.println("Nilai Double = " + nilaiDouble);

        // Tipe data logika

        /* Boolean ( Hanya memiliki dua nilai : true dan false) {
            > Default value = false
        } */
        boolean nilaiBoolean = true;
        System.out.println("Nilai Boolean = " + nilaiBoolean);
        
        // Tipe data karakter

        /* Char ( Menyimpan Singgle Karakter ) {
            > ukuran memori 16 bit
            > menyimpan karakter tunggal 
            > menggunakan singgle quote [ ' ]
        } */
        char nilaiChar = 'A';
        System.out.println("Nilai Char = " + nilaiChar);

        /* String */






    }
}
